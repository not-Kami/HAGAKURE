 <h4>L’Hagakure</h4>

 <p>L'origine et la philosophie du nom Hagakure
 Le terme Hagakure, signifiant « à l’ombre des feuilles », provient des écrits de Tsunetomo Yamamoto, ancien samouraï devenu moine en 1709. Pendant 7 ans, il livre ses pensées, influencées par le bushido (code d’honneur du samouraï) et le confucianisme, à un scribe. Ses enseignements, rassemblés en 11 tomes, servaient initialement de guide moral pour les samouraïs du clan Nabeshima. Restée confidentielle, l’œuvre ne sera révélée au grand public qu’au XXe siècle.

<img src="/public/assets/img/hagakure_img4.png" alt="XXe siècle">

Le nom Hagakure fait référence à la vie discrète et modeste de Yamamoto, mais aussi à la profondeur des écrits, dont le sens se dévoile entre les lignes.

<p>Le choix du nom pour le club</p>
En 1968, les fondateurs du club adoptent le nom Hagakure Karate Dojo, puis Hagakure Karate Club, en hommage à ce guide emblématique. Cette référence souligne l’attachement du club à l’éthique martiale et aux valeurs traditionnelles du karaté :

Humilité et sobriété, à l’image de la signification littérale « caché sous le feuillage ».
Rigueur et conduite irréprochable, inspirées par le code d’honneur du samouraï.
Hagakure KC reste fidèle à ces principes, transmettant un karaté ancré dans la tradition et l’esprit martial, où philosophie et technique se rencontrent sur les tatamis.

"Celui qui travaille âprement et sait rester modeste sera grandement estimé"  Jocho Yamamoto

