    <h4>LE CLUB</h4>
    <p>Créé à la fin des années 60 par Jean-Marie Kiebaum, Hagakure KC est l’un des premiers clubs belges de karaté, affilié à la Japan Karate Association (JKA)
 
    Les premiers cours se tenaient à Waterloo, avant d’être déplacés à la Maison des Jeunes de Braine-l'Alleud sous la direction d’Eric Feron. En 1979, Christian Debroux reprend la direction du club après la disparition tragique de ses prédécesseurs.

 <img src="/public/assets/img/hagakure_img1.png" alt="prédecesseurs">
 
 Depuis 1986, le dojo est établi au Hall omnisport de Braine-l'Alleud, accueillant une soixantaine de membres de tous âges.

 <img src="/public/assets/img/hagakure_imglogo.png" alt="Logo">
  
 <p>Une équipe d’instructeurs expérimentés</p>
 Sous la direction de Christian Debroux (5e Dan JKA), le club bénéficie de l’expertise de Joël Vandommele, Serge Dupan, Kim Demets (tous 5e Dan JKA) et Arnaud Vicaire (4e Dan JKA).
  
 <p>Des parrains prestigieux</p>
 Hagakure KC entretient des liens étroits avec Kazuhiro Sawada Sensei et Raymond Honoré Sensei, qui dispensent régulièrement des cours spéciaux. Le club garde également un profond respect pour Satoshi Miyazaki Sensei, figure marquante du karaté belge, dont l’influence reste vive.</p>

