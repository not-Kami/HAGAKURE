    <h4>LE CLUB</h4>
    <p>La fin des années 60 voit la naissance d'Hagakure KC, un des premiers clubs belges de karaté reconnu par la Japan Karate Association.
 
 Le club est fondé par Jean-Marie Kiebaum et les cours se donnent à la salle Sainte-Thérèse de Waterloo jusqu'au décès inopiné du sensei.
 
 C'est Eric Feron qui reprend le flambeau durant quelques années et établi son dojo au sein de la maison des jeunes de Braine-l'Alleud.
 
 Suite au drame lié à la disparition prématurée du nouveau sensei, Christian Debroux passe à la direction d'Hagakure KC en 1979.

 <img src="/public/assets/img/hagakure_img1.png" alt="Hagakure KC 1970">
 
 Depuis 1986 le dojo se trouve au Hall omnisport de Braine-l'Alleud et ce n'est pas moins d'une septantaine de membres, jeunes et moins jeunes, qui fréquentent assidument les cours.

 <img src="/public/assets/img/hagakure_imglogo.png" alt="Logo">
  
 Christian Debroux Sensei, 5ème Dan JKA, dispense son enseignement secondé par Joël Vandommele, 5ème Dan JKA, Serge Dupan, 5ème Dan JKA, Kim Demets, 5ème Dan JKA et Arnaud Vicaire, 4ème Dan JKA. 
  
 Il est à noter que très tôt l'histoire du club s'est vue fortement influencée par le parrainage de Kazuhiro SAWADA Sensei et de Raymond HONORE Sensei qui entretiennent des contacts privilégiés avec les instructeurs d'Hagakure KC et nous font régulièrement profiter de leur savoir à l'occasion de cours spéciaux. 

 Autre personnage emblématique d'Hakagure KC et père du karaté belge, Satoshi MIYAZAKI Sensei (17.06.1938 / 31.05.1993) marqua à jamais de son style et de sa personnalité la mémoire des plus anciens de nos membres.</p>

