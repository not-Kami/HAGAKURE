 <h4>L’Hagakure</h4>

 <p>En 1709, Tsunetomo Yamamoto, ou Jocho Yamamoto, ancien samouraï du clan Nabeshima alors devenu moine, se retire en montagne suite à la mort de son seigneur afin d’y vivre une vie recluse. Accompagné par un scribe pendant 7 ans, il lui livrera ses pensées, influencées par le bushido (code d’honneur du samouraï) et le confucianisme.

Rassemblés en recueil de onze tomes, ces enseignements, réflexions et autres anecdotes seront alors distribués aux samouraïs du clan Nabeshima, malgré la réticence de Yamamoto, afin de servir de guide moral et pratique.

Longtemps gardée secret, l’œuvre ne sera révélée au public qu’au début du XXème siècle sous le nom d’Hagakure. Littéralement, « à l’ombre des feuilles ».

<img src="/public/assets/img/hagakure_img4.png" alt="Tsunetomo 'Jocho Yamamoto'">

Pour certains, le recueil fut ainsi nommé en référence à la vie recluse et cachée de Yamamoto Tsunetomo. Pour d’autres, parce que le sens et la richesse des écrits se dissimulent entre les pages et entre les mots.

Hagakure est donc le nom qui fut donné, dès 1968, à notre club par ses fondateurs (Hagakure Karate Dojo dans un premier temps, Hagakure Karate Club ensuite). 

Référence au guide de Yamamoto et au code d’honneur du samouraï, bien sûr. Rigoureux, parfois abscons dans notre société contemporaine, celui-ci exige une conduite digne, irréprochable et désintéressée. Bien entendu, sa lecture et son interprétation doivent être replacées dans son contexte original, guerrier et féodal. Mais les enseignements de l'Hagakure demeurent des pistes philosophiques pouvant faire écho à l'éthique martiale exigée sur les tatamis.

Référence au sens littéral du terme aussi. « A l’ombre des feuilles » ou « caché sous le feuillage » renvoit à un comportement humble et modeste, fuyant la gloriole et recherchant les richesses du karaté dans la sobriété et l'immuabilité des valeurs traditionnelles.
 
De fait, Hagakure souligne l'ancrage de notre club dans la tradition du karaté et l'importance conférée par nos instructeurs à l'esprit martial.

"Celui qui travaille âprement et sait rester modeste sera grandement estimé"
                                                                                            Jocho Yamamoto

