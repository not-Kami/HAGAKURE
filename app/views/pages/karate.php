    <h3>Le karaté : un art martial traditionnel japonais</h3>

        <p>
         Le karaté, ou karate-do, est un art martial d’origine okinawaienne et japonaise, appartenant au genre atemi-waza (« techniques de frappe »). Il repose sur des percussions, blocages, déplacements et balayages utilisant les armes naturelles du corps humain : poings, pieds, coudes, genoux, etc.
        </p>
        <img src="/public/assets/img/karate01.png" alt="genoux, etc">
    <h3>Trois piliers fondamentaux du karaté </h3>
        <p>
        L’apprentissage du karaté se structure autour de trois axes complémentaires :

        Kihon : techniques de base, répétées seul avec la rigueur du combat.
        Kata : enchaînements codifiés représentant des séquences de combat.
        Kumite : combat à plusieurs niveaux – conventionnel (kihon kumite), semi-conventionnel (jyu ippon kumite) ou libre (jyu kumite).
        Le karaté en compétition
        En compétition, les pratiquants s’affrontent en kata ou en jyu kumite (combat libre), avec gants et protège-dents obligatoires. Bien que physiquement et mentalement exigeante, la compétition reste une option et non une finalité, offrant une perspective complémentaire aux karatékas motivés.
        </p>

    <h3>L’histoire du karaté : des origines à la modernité</h3>
        <p>
        Le karaté puise ses racines en Inde, puis en Chine, avant de se développer à Okinawa et au Japon. Né de la fusion entre les techniques chinoises (To-De) et les méthodes de combat indigènes d’Okinawa, le karaté devient une redoutable technique d’autodéfense, pratiquée dans le plus grand secret après l’interdiction des armes par les samouraïs.

        <img src="/public/assets/img/histoire01.php" alt="samouraïs">

        Okinawa-te, littéralement « Main d’Okinawa », s’organise en trois styles majeurs : Shuri-te, Naha-te et Tomari-te. L’introduction du Bubishi, un manuscrit chinois répertoriant les principales techniques, marque un tournant dans le développement du karaté.

        La transformation vers le Karate-Do
        Au début du XXe siècle, le karaté évolue de technique de survie vers un art martial éducatif et spirituel. En 1922, Funakoshi Gishin, considéré comme le père du karaté moderne, présente cet art au Japon. Il rebaptise Okinawa-te en karate-do (« La Voie de la Main Vide »), insistant sur sa dimension philosophique.

        <img src="/public/assets/img/histoire02.php" alt="philosophique">

        Le style Shotokan, développé par les élèves de Funakoshi, devient l’un des plus répandus, caractérisé par des positions basses, longues et une recherche d’efficacité (ippon). Dès 1960, la Japan Karate Association (JKA) diffuse ce style à l’international.

        <img src="/public/assets/img/histoire.png" alt="international">

        Karaté traditionnel vs karaté sportif
        L’après-guerre voit l’émergence de deux 
        courants distincts :
        Traditionnel, axé sur l’éthique martiale et la transmission des valeurs ancestrales.
        Sportif, intégrant le karaté aux compétitions et aux programmes éducatifs.
        Bien que le karaté soit aujourd’hui largement pratiqué comme sport, il reste avant tout un art martial, ancré dans des traditions millénaires, où chaque kata et chaque mouvement sont un lien vivant avec son riche passé.

        <img src="/public/assets/img/histoire04.png" alt="passé">

        Sources: R. Habersetzer, Karate Pratique, Ed. Amphora

         M. Nakayama, Best Karate: Introduction, Ed. Vigot
        </p>

    <h3>Dojokun : les règles fondamentales du dojo</h3>

        <p>
        Le Dojokun (« règles du dojo ») est un code de conduite essentiel dans tous les arts martiaux japonais, dont le karaté Shotokan. Il définit les comportements à adopter avant, pendant et après l’entraînement.

        <img src="/public/assets/img/dokojun01.png" alt="entrainement">

        Souvent affiché près du kamiza (le mur d’honneur où figure le portrait du Maître), il est parfois récité en début de séance pour rappeler les valeurs fondamentales : respect, discipline et éthique martiale.

        Les cinq règles du Dojokun Shotokan, héritées des premiers instructeurs de la Japan Karate Association (JKA), soulignent l’importance d’une attitude exemplaire sur et hors du tatami.

        <img src="/public/assets/img/dokojun02.png" alt="tatami">

        </p>

    <h3>JKA : Gardienne du karaté traditionnel Shotokan</h3>

        <p>
        La Japan Karate Association (JKA), fondée en 1949 par les élèves de Gishin Funakoshi, est la plus grande et la plus respectée organisation de karaté Shotokan au monde. Reconnue par le ministère de l’Éducation nationale japonais depuis 1957, elle veille à préserver les traditions du karaté-do tout en développant un enseignement rigoureux et structuré.

        <img src="/public/assets/img/hjka.png" alt="structuré">

        Son programme d’entraînement unique a formé des instructeurs professionnels de haut niveau, ambassadeurs du karaté dans 109 pays. Parmi eux, Masatoshi Nakayama, chef instructeur dès 1958, a joué un rôle clé dans l’expansion mondiale de la JKA et la promotion des compétitions sportives.

        <img src="/public/assets/img/hjka02.png" alt="sportives">

        En Belgique, Satoshi Miyazaki, élève de Nakayama, devient chef instructeur en 1967. Redoutable combattant, il est connu pour ses entraînements exigeants et sa vision authentique du karaté. Il laisse un héritage durable, transmis par ses élèves depuis sa disparition en 1993.

        <img src="/public/assets/img/hjka03.png" alt="en 1993">

        </p>
